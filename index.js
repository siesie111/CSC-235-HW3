// count # of languages per family
let familyCount = d3.rollup(myData, v => v.length, d => d.family);
let barData = Array.from(familyCount, ([family, count]) => ({ family, count }))
                   .sort((a, b) => b.count - a.count);

// bar chart
let width  = 600;
let height = 450;
let margin = 80;

let frame = d3.select("#bar-chart")
                .append("svg")
                .attr("width", width)
                .attr("height", height);

// chart title
frame.append("text")
    .attr("x", width / 2)
    .attr("y", 20)
    .attr("text-anchor", "middle")
    .style("font-size", "16px")
    .style("font-weight", "bold")
    .text("Number of Languages per Family");

// x axis label
frame.append("text")
    .attr("x", width / 2)
    .attr("y", height - 7)
    .attr("text-anchor", "middle")
    .style("font-size", "12px")
    .style("font-weight", "bold") 
    .text("Language Family");

// y axis label
frame.append("text")
    .attr("transform", "rotate(-90)")
    .attr("x", -(height / 2))
    .attr("y", 45)
    .attr("text-anchor", "middle")
    .style("font-size", "12px")
    .style("font-weight", "bold") 
    .text("Count");

// scales
let xScale = d3.scaleBand()
                .domain(barData.map(d => d.family))
                .range([margin, width - margin])
                .paddingInner(0.2);

let yScale = d3.scaleLinear()
                .domain([0, d3.max(barData, d => d.count)])
                .range([height - margin, margin]);

// axes
frame.append("g")
    .attr("transform", `translate(0, ${height - margin})`)
    .call(d3.axisBottom(xScale))
    .selectAll("text")
        .attr("transform", "rotate(-35)")
        .style("text-anchor", "end");

frame.append("g")
    .attr("transform", `translate(${margin}, 0)`)
    .call(d3.axisLeft(yScale).ticks(5));

// bars
let highlighted = null;

frame.selectAll("rect")
    .data(barData)
    .join("rect")
    .attr("x", d => xScale(d.family))
    .attr("y", d => yScale(d.count))
    .attr("width", xScale.bandwidth())
    .attr("height", d => (height - margin) - yScale(d.count))
    .attr("fill", "mediumseagreen")
    .on("click", function(event, d) {
        if (highlighted === d.family) {
            // click again to un-highlight
            d3.selectAll("rect").attr("fill", "mediumseagreen").attr("opacity", 1);
            highlighted = null;
        } else {
            d3.selectAll("rect").attr("fill", "mediumseagreen").attr("opacity", 0.4);
            d3.select(this).attr("fill", "coral").attr("opacity", 1);
            highlighted = d.family;
        }
    });


// pie chart
let pieWidth  = 400;
let pieHeight = 400;
let pieRadius = 150;

let pieSvg = d3.select("#pie-chart")
                .append("svg")
                .attr("width", pieWidth)
                .attr("height", pieHeight)
                .append("g")
                .attr("transform", `translate(${pieWidth / 2}, ${pieHeight / 2})`);

// chart title
pieSvg.append("text")
    .attr("y", -pieHeight / 2 + 20)
    .attr("text-anchor", "middle")
    .style("font-size", "16px")
    .style("font-weight", "bold")
    .text("Proportion of Languages per Family");

let colour = d3.scaleOrdinal(d3.schemeTableau10);
let pie = d3.pie().value(d => d.count);
let arc = d3.arc().innerRadius(0).outerRadius(pieRadius);
let total = d3.sum(barData, d => d.count);

// slices
pieSvg.selectAll("path")
    .data(pie(barData))
    .join("path")
    .attr("d", arc)
    .attr("fill", d => colour(d.data.family))
    .attr("stroke", "white")
    .style("stroke-width", "1px")
    .on("click", function(event, d) {
        let pct = ((d.data.count / total) * 100).toFixed(1);
        d3.select("#pie-info")
            .text(d.data.family + ": " + pct + "% (" + d.data.count + " languages)");
    });

// slice labels 
pieSvg.selectAll("text.label")
    .data(pie(barData))
    .join("text")
    .attr("class", "label")
    .attr("transform", d => "translate(" + arc.centroid(d) + ")")
    .attr("text-anchor", "middle")
    .style("font-size", "11px")
    .style("fill", "white")
    .style("display", d => (d.endAngle - d.startAngle > 0.8) ? null : "none")
    .text(d => d.data.family);












